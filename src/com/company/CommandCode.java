package com.company;

public class CommandCode {
    //DatenTyp ArrayList
    String pos;
    String befehl;
    double x;
    double y;
    double i;
    double j;

    public CommandCode(String pos, String befehl, double x, double y, double i, double j) {
        this.pos = pos;
        this.befehl = befehl;
        this.x = x;
        this.y = y;
        this.i = i;
        this.j = j;
    }
}
